        dynamicProp: {},
      }
    },
    created() {
		let menus = menu.list()
		for(let x in menus){
			if(menus[x].tableName == this.userTableName){
				this.menuList = menus[x].backMenu
			}
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'huiyuanzhanghao', null);
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'huiyuanxingming', null);
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'mima', null);
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'xingbie', null);
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'touxiang', null);
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'dianhuahaoma', null);
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'huiyuannianxian', null);
		}
		if ('huiyuan' == this.userTableName) {
			this.$set(this.sessionForm, 'money', null);
		}

		if ('huiyuan' == this.userTableName&&this.rules['huiyuanzhanghao']){
			this.rules['huiyuanzhanghao'].push({ required: true, message: '请输入会员账号', trigger: 'blur' })
		}else if('huiyuan' == this.userTableName&&!this.rules['huiyuanzhanghao']) {
			this.$set(this.rules, 'huiyuanzhanghao', [{ required: true, message: '请输入会员账号', trigger: 'blur' }]);
		}
		if ('huiyuan' == this.userTableName&&this.rules['huiyuanxingming']){
			this.rules['huiyuanxingming'].push({ required: true, message: '请输入会员姓名', trigger: 'blur' })
		}else if('huiyuan' == this.userTableName&&!this.rules['huiyuanxingming']) {
			this.$set(this.rules, 'huiyuanxingming', [{ required: true, message: '请输入会员姓名', trigger: 'blur' }]);
		}
